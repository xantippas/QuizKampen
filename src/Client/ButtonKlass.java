package Client;

import javax.swing.*;

public class ButtonKlass extends String {

    String fråga;
    String alternativ;
    String svar;

    public String getFråga() {
        return fråga;
    }

    public void setFråga(String fråga) {
        this.fråga = fråga;
    }

    public String getAlternativ() {
        return alternativ;
    }

    public void setAlternativ(String alternativ) {
        this.alternativ = alternativ;
    }

    public String getSvar() {
        return svar;
    }

    public void setSvar(String svar) {
        this.svar = svar;
    }
}
